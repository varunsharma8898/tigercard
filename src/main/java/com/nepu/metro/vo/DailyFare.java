package com.nepu.metro.vo;

public class DailyFare {

    private int totalFare;

    private int applicableDailyCap;

    public int getTotalFare() {
        return totalFare;
    }

    public void setTotalFare(int totalFare) {
        this.totalFare = totalFare;
    }

    public int getApplicableDailyCap() {
        return applicableDailyCap;
    }

    public void setApplicableDailyCap(int applicableDailyCap) {
        this.applicableDailyCap = applicableDailyCap;
    }
}
